
namespace task_2
{
  /// <summary>
  /// This class calculates square.
  /// </summary>
  public class Square
  {
    /// <summary>
    /// This method calculates square.
    /// </summary>
    /// <param name="length">Rectangles length.</param>
    /// <param name="wedth">Rectangle wedth.</param>
    /// <returns></returns>
    public double CalculateSquare(double length, double wedth)
    {
      return length * wedth;
    }
  }
